/**
 * UI language — Arabic-first (RTL) with an English toggle. One flat
 * dictionary; components subscribe via useUILang() so t() stays reactive.
 */
import { useSyncExternalStore } from "react";

export type UILang = "ar" | "en";
const KEY = "quran-studio:ui-lang";

let lang: UILang = (localStorage.getItem(KEY) as UILang) || "ar";
const listeners = new Set<() => void>();

export function applyUILang(): void {
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
}

export function setUILang(l: UILang): void {
  lang = l;
  localStorage.setItem(KEY, l);
  applyUILang();
  listeners.forEach((cb) => cb());
}

export function useUILang(): UILang {
  return useSyncExternalStore(
    (cb) => {
      listeners.add(cb);
      return () => listeners.delete(cb);
    },
    () => lang,
  );
}

export const getUILang = (): UILang => lang;

/** Localized digits: Arabic-Indic in Arabic UI. */
export const num = (n: number | string): string =>
  lang === "ar" ? String(n).replace(/\d/g, (d) => "٠١٢٣٤٥٦٧٨٩"[Number(d)]) : String(n);

